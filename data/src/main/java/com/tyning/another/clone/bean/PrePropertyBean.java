package com.tyning.another.clone.bean;

public class PrePropertyBean {
    private int a = 0;
    private Integer b = 0;
    private String c = "asd";

    public PrePropertyBean(int a, Integer b, String c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public Integer getB() {
        return b;
    }

    public void setB(Integer b) {
        this.b = b;
    }

    public String getC() {
        return c;
    }

    public void setC(String c) {
        this.c = c;
    }
}
